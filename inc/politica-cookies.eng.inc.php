<h2>TYPES OF COOKIES </h2>
<h3> THE ENTITY THAT MANAGES THEM </h3>
<h4>Own cookies </h4>
<p>These are those collected by the publisher to provide the service requested by the user. </p>
<h4>Third-party cookies </h4>
<p>These are those that are collected and managed by a third party, these cannot be considered own.</p>
<h3>THE PERMANENT TIME PERMANCING TIME ACTIVATED </h3>
<h4>Session cookies</h4>
<p>Receive data while the user surfs the net in order to provide the requested service.</p>
<h4>Persistent Cookies </h4>
<p>They are stored in the terminal and the information obtained will be used by the person responsible for the cookie in order to provide the service requested.</p>
<h3>THEIR PURPOSE </h3>
<h4>Technical cookies</h4>
<p>They are necessary for the correct navigation through the web.</p>
<h4>Personalization Cookies </h4>
<p>Allows the user the characteristics (language) for browsing the website </p>
<h4>Analysis Cookies </h4>
<p>allow the provider the analysis linked to navigation made by the user, in order to track the use of the website, as well as statistics of the most visited content, number of visitors, etc.</p>
<h4>Advertising cookies</h4>
<p>Allows the publisher to include in the web, advertising spaces, according to the content of the web itself. </p>
<h4>Behavioural advertising cookies</h4>
<p>Enable the publisher to include in the website advertising spaces according to the information obtained through the user's browsing habits.</p>
